bool ff(string s) {
    s+=')';
    int cnt = 0;
    int n = s.size();
    int y = 0;
    for(int i =0;i < n - 1;i++) {
        if(s[i] =='0' && s[i + 1] >='0' && s[i + 1] <='9') {
            return 0;
        }
    }
    char z ='+';
    for(int i =0;i <n;i++) {
        if(s[i] <= '9' && s[i] >= '0') {
            y = y*10 + (s[i] - '0');
        } else {
            if(s[i] != ')' && s[i] !='(') {
                z = s[i];
            } 
            if(s[i] ==')') {
                if(z =='-') {
                    cnt-=y;
                    y =0;
                }
                if(z=='+'){
                    cnt+=y;
                    y =0;
                }
                if(z=='*'){
                    cnt*=y;
                    y =0;
                }
            }
        }
    }
    if(cnt == 100) {
        return 1;
    } else {
        return 0;
    }
     
}
